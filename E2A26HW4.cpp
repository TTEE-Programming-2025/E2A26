// 安全輸入整數且限定範圍
int inputIntInRange(const char* prompt, int min, int max) {
    int num;
    char term;
    while (1) {
        printf("%s", prompt);
        if (scanf("%d%c", &num, &term) != 2 || term != '\n') {
            printf("輸入錯誤，請輸入整數。\n");
            while (getchar() != '\n');
            continue;
        }
        if (num < min || num > max) {
            printf("輸入數字必須介於 %d 到 %d。\n", min, max);
            continue;
        }
        return num;
    }
}

// 輸入學生資料
void inputGrades() {
    clearScreen();
    int n;
    printf("請輸入學生人數 (5~10): ");
    while (scanf("%d", &n) != 1 || n < 5 || n > 10) {
        printf("輸入錯誤，請輸入 5~10 之間的整數: ");
        while (getchar() != '\n'); // 清除錯誤輸入
    }
    student_count = n;
    getchar(); // 吃掉換行符號，避免影響後面 fgets

    for (int i = 0; i < student_count; i++) {
        printf("第 %d 位學生資料輸入：\n", i + 1);

        // 姓名
        while (1) {
            printf("姓名：");
            fgets(students[i].name, NAME_LENGTH, stdin);
            students[i].name[strcspn(students[i].name, "\n")] = 0; // 移除換行

            if (strlen(students[i].name) == 0) {
                printf("姓名不可為空，請重新輸入。\n");
            } else {
                break;
            }
        }

        // 學號
        printf("學號 (6位數整數)：");
        while (scanf("%d", &students[i].id) != 1 || students[i].id < 100000 || students[i].id > 999999) {
            printf("輸入錯誤，請輸入 6 位數學號：");
            while (getchar() != '\n'); // 清空錯誤輸入
        }

        // 數學成績
        printf("數學成績 (0~100)：");
        while (scanf("%d", &students[i].math) != 1 || students[i].math < 0 || students[i].math > 100) {
            printf("輸入錯誤，請輸入 0~100：");
            while (getchar() != '\n');
        }

        // 物理成績
        printf("物理成績 (0~100)：");
        while (scanf("%d", &students[i].physics) != 1 || students[i].physics < 0 || students[i].physics > 100) {
            printf("輸入錯誤，請輸入 0~100：");
            while (getchar() != '\n');
        }

        // 英文成績
        printf("英文成績 (0~100)：");
        while (scanf("%d", &students[i].english) != 1 || students[i].english < 0 || students[i].english > 100) {
            printf("輸入錯誤，請輸入 0~100：");
            while (getchar() != '\n');
        }

        // 計算平均
        students[i].avg = (students[i].math + students[i].physics + students[i].english) / 3.0;

        while (getchar() != '\n'); // 清除多餘換行，準備下一位學生輸入
    }

    pauseScreen();
    clearScreen();
}

// 顯示所有學生資料
void displayGrades() {
    clearScreen();
    if (student_count == 0) {
        printf("沒有學生資料，請先輸入。\n");
        pauseScreen();
        clearScreen();
        return;
    }
    printf("所有學生資料如下：\n");
    printf("姓名        學號     數學  物理  英文  平均成績\n");
    printf("-------------------------------------------------\n");
    for (int i = 0; i < student_count; i++) {
        printf("%-14s%06d   %3d   %3d   %3d   %.1f\n",
               students[i].name, students[i].id,
               students[i].math, students[i].physics, students[i].english,
               students[i].avg);
    }
    pauseScreen();
    clearScreen();
}

// 搜尋學生資料
void searchGrades() {
    char searchName[NAME_LENGTH];
    int found = 0;

    clearScreen();

    // 提示使用者輸入
    printf("請輸入要搜尋的學生姓名：");
	
	while (getchar() != '\n'); 

    // 使用 fgets 讀入整行，並移除換行字元
    fgets(searchName, sizeof(searchName), stdin);
    searchName[strcspn(searchName, "\n")] = '\0'; // 移除 \n

    // 搜尋所有學生資料
    for (int i = 0; i < student_count; i++) {
        if (strcmp(students[i].name, searchName) == 0) {
            printf("查詢結果：\n");
            printf("姓名：%s\n", students[i].name);
            printf("學號：%d\n", students[i].id);
            printf("數學：%d\n", students[i].math);
            printf("物理：%d\n", students[i].physics);
            printf("英文：%d\n", students[i].english);
            printf("平均成績：%.1f\n", students[i].avg);
            found = 1;
            break;
        }
    }

    // 如果找不到
    if (!found) {
        printf("查無此學生資料。\n");
    }

    // 等待按鍵後清除畫面
    pauseScreen();
    clearScreen();
}

// 成績排名
void gradeRanking() {
    clearScreen();
    if (student_count == 0) {
        printf("沒有學生資料，請先輸入。\n");
        pauseScreen();
        clearScreen();
        return;
    }

    Student temp[MAX_STUDENTS];
    memcpy(temp, students, sizeof(Student) * student_count);

    for (int i = 0; i < student_count - 1; i++) {
        for (int j = 0; j < student_count - 1 - i; j++) {
            if (temp[j].avg < temp[j+1].avg) {
                Student t = temp[j];
                temp[j] = temp[j+1];
                temp[j+1] = t;
            }
        }
    }

    printf("成績排名 (依平均成績由高至低)：\n");
    printf("姓名           學號     平均成績\n");
    printf("---------------------------------\n");
    for (int i = 0; i < student_count; i++) {
        printf("%-14s %06d   %.1f\n", temp[i].name, temp[i].id, temp[i].avg);
    }

    pauseScreen();
    clearScreen();
}

// 離開確認
int confirmExit() {
    char yn;
    while (1) {
        printf("確定離開？ (y/n): ");
        getchar();
        yn = getchar();
        if (yn == 'y' || yn == 'Y') {
            return 1;
        } else if (yn == 'n' || yn == 'N') {
            return 0;
        } else {
            printf("輸入錯誤，請輸入 'y' 或 'n'。\n");
        }
    }
}

int main() {
    const char correct_password[] = "2025";
    char input[10];
    int attempt = 0;

    showPersonalStyle();

    while (attempt < 3) {
        printf("久等了 請輸入您的密碼 : ");
        scanf("%4s", input);

        if (strcmp(input, correct_password) == 0) {
            printf("密碼正確，24歲，是學生\n");
            clearScreen();
            break;
        } else {
            printf("哈?密碼錯誤！\n");
            attempt++;
        }
    }

    if (attempt == 3) {
        printf("三回啊三回，踢出程式不可避。\n");
        return 0;
    }

    char choice;
    do {
        showMenu();
        printf("嗯?請選擇 (a~e): ");
        scanf(" %c", &choice);  // 注意這裡的空白，跳過換行

        switch(choice) {
            case 'a':
                inputGrades();
                break;
            case 'b':
                displayGrades();
                break;
            case 'c':
                searchGrades();
                break;
            case 'd':
                gradeRanking();
                break;
            case 'e':
                if (confirmExit()) {
                    printf("離開系統，感謝使用喔♂！\n");
                    return 0;
                } else {
                    clearScreen();
                }
                break;
            default:
                printf("無效選項，請重新輸入。\n");
                pauseScreen();
                clearScreen();
                break;
        }
    } while (1);

    return 0;
}

